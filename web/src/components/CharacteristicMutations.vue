<template>
<div>
  <div class="d-flex align-items-center justify-content-between mb-1 mr-4">
    <div class="d-flex flex-column">
      <h4 class="mb-0">{{ definitionLabel }}</h4>
      <small class="text-muted">Mutations in at least {{thresholdFormatted}} of {{mutationName}} sequences <router-link v-if="reportType != 'mutation'" :to="{name: 'SituationReportMethodology', hash: '#characteristic'}" target="_blank">(read more)</router-link></small>
    </div>

    <div class="d-flex flex-column align-items-end">
      <router-link v-if="lineageName" :to="{name:'SituationReportComparison', query: { pango: lineages }}">Compare to other lineages</router-link>
      <router-link class="mt-n1" v-if="lineageName" :to="{name:'SituationReportComparison', query: { pango: lineageName, gene: 'S', threshold: 0.2  }}">View S-gene mutations</router-link>
    </div>

  </div>

  <SARSMutationMap :mutationKey="mutationName" :lineageMutations="mutations" :additionalMutations="additionalMutations" class="mb-3" v-if="mutations || additionalMutations" :copyable="true" />

  <div class="d-flex align-items-center ml-2 mr-3">
    <DownloadReportData :data="data" figureRef="mutation-map" dataType="Mutation Map" />
  </div>

  <div class="ml-2" id="mutation-table">
    <div class="row">
      <div class="col" v-if="data">
        <MutationTable :data="data" :lineageTotal="lineageTotal" :moc="moc" :moi="moi" :threshold="threshold" :colorScale="colorScale" :lineageName="mutationName" v-if="colorScale" />
      </div>
    </div>
  </div>
</div>
</template>

<script>
import Vue from "vue";
// --- store / Vuex ---
import {
  mapState
} from "vuex";

import {
  format,
  scaleOrdinal
} from "d3";

import SARSMutationMap from "@/components/SARSMutationMap.vue";
import MutationTable from "@/components/MutationTable.vue";
import DownloadReportData from "@/components/DownloadReportData.vue";
import NT_MAP from "@/assets/genomics/sarscov2_NC045512_genes_nt.json";

import {
  getBadMutations
} from "@/api/genomics.js";

export default {
  name: "CharacteristicMutations",
  computed: {
    lineages() {
      if(this.sublineages){
        return([this.lineageName].concat(this.sublineages))
      }
      return(this.lineageName)
    },
    data() {
      return(this.mutations.concat(this.additionalMutations))
    }
  },
  props: {
    mutations: Array,
    definitionLabel: String,
    mutationName: String,
    lineageName: String,
    lineageTotal: [Number, String],
    thresholdFormatted: String,
    threshold: [Number, String],
    sublineages: [Array, String],
    reportType: String,
    additionalMutations: Array
  },
  components: {
    SARSMutationMap,
    MutationTable,
    DownloadReportData
  },
  data() {
    return ({
      colorScale: null,
      moi: null,
      moc: null,
      colorDomain: ["#bab0ab", // lt grey -- UTRs
        "#1f77b4", // dk blue
        "#aec7e8", // lt blue
        "#f28e2c", // orange
        "#e15759", // red
        "#9edae5", // teal
        "#59a14f", // green
        "#edc949", // yellow
        "#9467bd", // purple
        "#ff9da7", // pink
        "#8c564b", // brown
        "#555555", // grey
        "#bcbd22", // puce
        "#bab0ab"
      ]
    })
  },
  mounted() {
    const ofInterest = getBadMutations();
    this.moc = ofInterest.moc;
    this.moi = ofInterest.moi;
    // convert object of nucleotides into an array
    this.ntMapArr = Object.entries(NT_MAP).map(d => {
      return {
        gene: d[0],
        start: d[1].start,
        end: d[1].end
      }
    })

    let geneNames = this.ntMapArr.sort((a, b) => a.start - b.start).map(d => d.gene);

    this.colorScale = scaleOrdinal(this.colorDomain)
      .domain(geneNames);
  }
}
</script>

